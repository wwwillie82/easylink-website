import assert from 'node:assert/strict';
import { adminNavItems, adminRouteRules, defaultNewUserPermissionMatrix, fullAdminPermissionMatrix, normalizePermissions, scopeActions } from '../src/lib/admin/permissions.mjs';
import { normalizePermissionMatrix, validateUserPayload, classifyUserMutation, isFullAdminMatrix } from '../src/lib/admin/users.mjs';
import { tokenHash, RESET_EXPIRES_MINUTES, GENERIC_RESET_MESSAGE } from '../src/lib/admin/password-reset.mjs';
import { usersHtml, forgotPasswordHtml, resetPasswordHtml } from '../src/lib/admin/render/users.mjs';
assert.equal(adminNavItems.find(i=>i.scope==='users')?.u1, true);
assert.equal(adminNavItems.find(i=>i.scope==='audit')?.u1, false);
assert(adminRouteRules.some(r=>r.pattern.test('/admin/users') && r.scope==='users'));
assert(adminRouteRules.some(r=>r.pattern.test('/api/admin/password-reset/request') && r.public));
const valid=validateUserPayload({display_name:' Teszt ',email:'USER@EXAMPLE.COM',role:'admin'} ,{create:true});
assert.equal(valid.email,'user@example.com'); assert.equal(valid.status,'active');
assert(!('role' in valid));
assert.deepEqual(normalizePermissionMatrix({media:{canDelete:true}}).media.canDelete,false);
assert.equal(normalizePermissionMatrix({pages:{canSave:true}}).pages.canSave,true);
assert.equal(normalizePermissionMatrix({pages:null}).pages, undefined);
assert.equal(isFullAdminMatrix(fullAdminPermissionMatrix), true);
assert.equal(isFullAdminMatrix(defaultNewUserPermissionMatrix), false);
assert.deepEqual(classifyUserMutation({status:'active'},{status:'disabled'}),{needsSave:false,needsArchive:true});
assert.match(tokenHash('secret'),/^[a-f0-9]{64}$/); assert.equal(RESET_EXPIRES_MINUTES,60); assert.match(GENERIC_RESET_MESSAGE,/aktív felhasználó/);
const html=usersHtml({permissions:{users:{canSave:true,canArchive:true}}}); assert.match(html,/Felhasználók/); assert.match(html,/Jelszóbeállító link/); assert.doesNotMatch(html,/role/i);
assert.doesNotMatch(usersHtml({permissions:{users:{}}}),/<button id="newUser">/);
assert.doesNotMatch(forgotPasswordHtml()+resetPasswordHtml('abc'),/password_hash|token_hash|role/);
console.log('admin-users U2 smoke ok');
