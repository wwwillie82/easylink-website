import assert from 'node:assert/strict';
import { existsSync } from 'node:fs';
import { mkdtemp, writeFile } from 'node:fs/promises';
import { join } from 'node:path';
import { tmpdir } from 'node:os';
import { copyMediaToRelease, detectImageMime, mediaConfig, normalizePublicBase, publicBaseToRelative, safeMediaFilename, storagePathForPublicPath, storeMediaFile, validateMediaFile } from '../src/lib/admin/media-storage.mjs';

const storageRoot = await mkdtemp(join(tmpdir(), 'easylink-media-storage-'));
const env = { SITE_MEDIA_STORAGE_DIR: storageRoot, SITE_MEDIA_PUBLIC_BASE_URL: '/assets/site-media', SITE_MEDIA_MAX_BYTES: '64' };
assert.equal(mediaConfig(env).storageRoot, storageRoot);
assert.equal(normalizePublicBase('/assets/site-media/'), '/assets/site-media');
assert.equal(publicBaseToRelative('/assets/site-media'), 'assets/site-media');
assert.throws(() => normalizePublicBase('assets/site-media'), /public/);
assert.throws(() => normalizePublicBase('/assets/../secret'), /public/);
assert.equal(safeMediaFilename('Árvíztűrő kép.JPG', Buffer.from('a1b2c3d4')), 'arvizturo-kep-61316232.jpg');
assert.throws(() => safeMediaFilename('../evil.png'), /fájlnév/);
const png = Buffer.from([0x89,0x50,0x4e,0x47,0x0d,0x0a,0x1a,0x0a,0,0]);
assert.equal(detectImageMime(png), 'image/png');
assert.equal(validateMediaFile({ filename: 'kep.png', contentType: 'image/png', buffer: png, maxBytes: 64 }).type, 'image/png');
assert.throws(() => validateMediaFile({ filename: 'kep.jpg', contentType: 'image/jpeg', buffer: png, maxBytes: 64 }), /tartalma/);
assert.throws(() => validateMediaFile({ filename: 'kep.svg', contentType: 'image/svg+xml', buffer: Buffer.from('<svg/>'), maxBytes: 64 }), /WebP/);
assert.throws(() => validateMediaFile({ filename: 'kep.png', contentType: 'image/png', buffer: Buffer.alloc(65), maxBytes: 64 }), /túl nagy/);
const mp4 = Buffer.concat([Buffer.from([0,0,0,24]), Buffer.from('ftypisom'), Buffer.alloc(16)]);
assert.equal(validateMediaFile({ filename: 'video.mp4', contentType: 'video/mp4', buffer: mp4, size: 100, maxBytes: 64, videoMaxBytes: 128 }).type, 'video/mp4');
assert.throws(() => validateMediaFile({ filename: 'video.mp4', contentType: 'application/octet-stream', buffer: mp4, videoMaxBytes: 128 }), /MIME/);
assert.throws(() => validateMediaFile({ filename: 'video.mp4', contentType: 'video/mp4', buffer: Buffer.from('not mp4'), videoMaxBytes: 128 }), /MP4/);
assert.throws(() => validateMediaFile({ filename: 'video.mp4', contentType: 'video/mp4', buffer: mp4, size: 129, videoMaxBytes: 128 }), /túl nagy/);
const stored = await storeMediaFile({ file: new File([png], 'Teszt Kép.png', { type: 'image/png' }), alt: 'Alt', env, now: new Date(Date.UTC(2026, 6, 10)) });
assert.match(stored.path, /^\/assets\/site-media\/2026\/07\/teszt-kep-[a-f0-9]{8}\.png$/);
assert.equal(existsSync(storagePathForPublicPath(stored.path, env)), true);
assert.throws(() => storagePathForPublicPath('/assets/site-media/2026/../evil.png', env), /útvonal/);
const release = await mkdtemp(join(tmpdir(), 'easylink-media-release-'));
await copyMediaToRelease({ releasePath: release, env });
assert.equal(existsSync(join(release, stored.path.replace(/^\//, ''))), true);
const missing = await copyMediaToRelease({ releasePath: release, env: { SITE_MEDIA_STORAGE_DIR: join(storageRoot, 'missing') } });
assert.equal(missing.skipped, true);
await writeFile(join(storageRoot, 'note.txt'), 'not image');
console.log('Media storage smoke passed: validation, safe paths, public URLs and publish copy.');
