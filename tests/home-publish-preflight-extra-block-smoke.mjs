import assert from 'node:assert/strict';
import { homeMiddleContentBlocks, validatePublishedHomeBlocksForSnapshot } from '../src/lib/content/home-blocks.mjs';
const pages=[{id:1,route:'/',type:'home',status:'published',title:'Home',hero_eyebrow:'e',hero_title:'t',hero_description:'d',hero_asset:'/a'}];
const validManual = [{id:98,page_id:1,block_key:'manual:valid-after',type:'text',title:'Extra',body:'Body',items:'[]',sort_order:1,status:'published'}];
assert.equal(homeMiddleContentBlocks({ page: { blocks: validManual } }).length, 1);
assert.deepEqual(validatePublishedHomeBlocksForSnapshot({ pages, blocks: validManual }), []);
const unknown = [{id:99,page_id:1,block_key:'manual:unknown',type:'unknown-widget',title:'Extra',body:'Body',items:'[]',sort_order:1,status:'published'}];
assert.equal(homeMiddleContentBlocks({ page: { blocks: unknown } }).length, 0);
const errors = validatePublishedHomeBlocksForSnapshot({ pages, blocks: unknown });
assert.equal(errors[0]?.code, 'HOME_PUBLISHED_UNKNOWN_BLOCK_TYPE');
assert.deepEqual(errors[0].details, { id: 99, blockKey: 'manual:unknown', block_key: 'manual:unknown', type: 'unknown-widget' });
console.log('home publish preflight manual-only smoke ok');
